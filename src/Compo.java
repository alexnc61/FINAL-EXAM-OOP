
public class Compo {
	private int tone, volume; 
	//compo disini sebagai composition pada electric guitar atau accoustic electric guitar
	
	public Compo(int tone, int volume) {
		super();
		this.tone = tone;
		this.volume = volume;
		
	}

	public int getTone() {
		return tone;
	}


	public int getVolume() {
		return volume;
	}


	
}
